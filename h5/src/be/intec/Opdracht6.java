package be.intec;

public class Opdracht6 {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 8;

        System.out.printf("number1 > number2 \t: " + (number1 > number2) + "\n");
        System.out.printf("number1 >= number2 \t: " + (number1 >= number2) + "\n");
        System.out.printf("number1 < number2 \t: " + (number1 < number2) + "\n");
        System.out.printf("number1 <= number2 \t: " + (number1 <= number2) + "\n");
        System.out.printf("number1 == number2 \t: " + (number1 == number2) + "\n");
        System.out.printf("number1 != number2 \t: " + (number1 != number2));
    }
}
