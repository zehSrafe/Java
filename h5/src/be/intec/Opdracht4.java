package be.intec;

public class Opdracht4 {
    public static void main(String[] args) {
        int number1 = 2;
        int number2 = 5;
        char aChar = 't';

        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number2 / number1);
        System.out.println(number1 % number2);
        aChar += number1;
        System.out.println(aChar);
    }
}
