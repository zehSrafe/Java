package be.intec;

public class Opdracht7 {
    public static void main(String[] args) {
        boolean bool1 = true;
        boolean bool2 = false;

        System.out.printf("bool1 \t:" + (bool1) + "\n");
        System.out.printf("bool2 \t: " + (bool2) + "\n");
        System.out.printf("bool1 && bool2 \t: " + (bool1 && bool2) + "\n");
        System.out.printf("bool1 || bool2 \t: " + (bool1 || bool2) + "\n");
        System.out.printf("!bool1 \t:" + (!bool1) + "\n");
        System.out.printf("!bool2 \t:" + (!bool2));
    }
}
